# Parametros End y Sep -------------------------------------------------------------------------------------

# Parametros end
print("Esto es un", end=" ")
print("ejemplo")                           # -> Esto es un ejemplo

# Parametro sep
print("1","2","3","4","5", sep="")         # controla los elementos que estan entre las separaciones 
                                           # -> 12345
print("1","2","3","4","5", sep="-")        # -> 1-2-3-4-5