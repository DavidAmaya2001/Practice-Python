# Operadores de Asignación ---------------------------------------------------------------

# Signo igual (=)
x = 5   # solo asigna el valor a la variable

# Asignación de Suma (+=)
x = 3
x += 5  # se asignara a la variable de manera de suma 3 + 5

# Asignación de Resta (-=)
x = 8
x -= 5  # se asignara a la variable de manera de resta 8 - 5

# Asignación de Multiplicación (*=)
x = 3
x *= 5  # se asignara a la variable de manera de producto 3 x 5

# Asignación de División (/=)
x = 10
x /= 3  # se asignara a la variable de manera divisora 10 / 3

# Asignación de División Entera (//=)
x = 10
x //= 3 # se asignara a la variable de manera divisora solo los enteros de 10 / 3

# Asignación de Exponente
x = 2
x **=5  # se asignara a la variable de manera de potencia 2 ^ 5 

# Asignación de Modulo o Resto (%=)
x = 10
x %= 2  # se asignara a la variable el modulo o resto de 10 % 2